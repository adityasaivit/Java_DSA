package loops;
import java.util.*;
public class practice_loop {

    public static void main(String args[])
    {
        Scanner scan=new Scanner(System.in);
        int n=scan.nextInt();
        for(int i=1;i<=n;i++)
        {
            System.out.print(i);
            System.out.print(' ');
        }
    }
    
}
